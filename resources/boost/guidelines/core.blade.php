## Traducciones en Español (laravel-lat-es)

Este proyecto tiene instalado `amendozaaguiar/laravel-lat-es`. Los archivos de traducción al español están disponibles
en `lang/es/`.

- Para publicar o actualizar las traducciones: `php artisan laravellates:install`
- Para cambiar el idioma: `'locale' => 'es'` en `config/app.php`
- Para usar traducciones en código: `__('auth.failed')`, `__('validation.required', ['attribute' => 'email'])`
- Activa la skill `laravel-lat-es` para obtener documentación completa de todas las claves disponibles.
